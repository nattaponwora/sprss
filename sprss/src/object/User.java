package object;

public class User {
	private int id;
	private String firstname;
	private String lastname;
	private int eid;
	private String email;
	private String position;
	
	public User(){
		
	}
	
	public int getID(){
		return id; 
	}
	
	public String getFirstname(){
		return firstname;
	}
	
	public String getLastname(){
		return lastname;
	}
	
	public int getEID(){
		return eid;
	}
	
	public String getEmail(){
		return email;
	}
	
	public String getPosition(){
		return position;
	}
}
